    private void txtBuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyPressed
        while (modeloArea.getRowCount() != 0) modeloArea.removeRow(0) ;

        String busqueda = txtBuscar.getText();
        int estado = cmbEstadoJefe.getSelectedIndex() + 1;

        String sql = "SELECT CONCAT(usuarios.nombres, ' ', usuarios.apellidos) AS nombre, usuarios.usuario, usuarios.correo, DATE_FORMAT(usuarios.fecha_registro, '%d - %m -%Y') AS registro, estado_usuario.estado FROM `usuarios` INNER JOIN estado_usuario ON usuarios.estado = estado_usuario.estado_usuario_id WHERE tipo_usuario = 2 AND (usuario LIKE '%" + busqueda + "%' OR CONCAT(nombres, '', apellidos) LIKE '%" + busqueda + "%') AND usuarios.estado = " + estado;

        conex.setRs(sql);

        try {
            listarJefesArea();
        } catch (SQLException ex) {
            System.out.println(ex);
            // Logger.getLogger(AlumnosListado.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtBuscarKeyPressed

