    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        String desde = "", hasta = "";
        
        int error = 0;
        
        try {
            desde = new SimpleDateFormat("yyyy-MM-dd").format(desdeJDate.getDate());
            hasta = new SimpleDateFormat("yyyy-MM-dd").format(hastaJDate.getDate());
        } catch (Exception e) {
            desde = null;
            hasta = null;
        }
        
        if (desde == null || hasta == null) {
            JOptionPane.showMessageDialog(null, "Rango de fechas invalidos", "Error al filtrar", JOptionPane.ERROR_MESSAGE);
            error++;
        }
        
        if (error < 1) { 
            String sql1 = "SELECT caso.caso_id, caso.codigo, tipo_solicitud.tipo, departamento.departamento, solicitud.caso, solicitud.descripcion AS requerimientos, caso.caso_descripcion AS datos, CONCAT(usuarios.nombres, ' ', usuarios.apellidos) AS programador, caso.fecha_limite AS entrega, estado_caso.estado FROM caso INNER JOIN solicitud ON solicitud.solicitud_id = caso.solicitud_id INNER JOIN usuarios ON usuarios.usuario_id = caso.programador INNER JOIN estado_caso ON estado_caso.estado_caso_id = caso.estado INNER JOIN tipo_solicitud ON solicitud.tipo_solicitud = tipo_solicitud.tipo_solicitud_id INNER JOIN departamento ON solicitud.departamento_id = departamento.departamento_id WHERE DATE(caso.fecha_registro) BETWEEN '" + desde + "' AND '" + hasta + "' ORDER BY caso.caso_id DESC";
            
            try {
                listarCasos(sql1);
            } catch (SQLException ex) {
                Logger.getLogger(InformesCasos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnFiltrarActionPerformed


