    private void cantidadCasos(String query) throws SQLException {
        cantidades = new ArrayList<>();
        
        conex.setRs(query);
        
        ResultSet rs = conex.getRs();
        
        while (rs.next()) {
            ArrayList<String> cantidad =  new ArrayList<>();
            
            cantidad.add(rs.getString(1));
            cantidad.add(rs.getString(2));
            
            cantidades.add(cantidad);
        }
        
        rs.close();
        
        lblAsignados.setText(cantidades.get(1).get(1));
        lblAprobados.setText(cantidades.get(0).get(1));
        lblRechazados.setText(cantidades.get(2).get(1));
        lblEspera.setText(cantidades.get(3).get(1));
    }
    
    private void listarCasos(String query) throws SQLException {
        modeloCasos.setRowCount(0);
        
        conex.setRs(query);
        
        ResultSet rs = conex.getRs();
        
        while (rs.next()) {
            Object[] newRow = {
                rs.getInt(1), 
                rs.getString(2), 
                rs.getString(3), 
                rs.getString(4), 
                rs.getString(5), 
                rs.getString(6), 
                rs.getString(7), 
                rs.getString(8), 
                rs.getString(9), 
                rs.getString(10),
            };
            
            modeloCasos.addRow(newRow);
        }
        
        rs.close();
    }
    
